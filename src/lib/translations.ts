export function t(key: string, lang: Language, replacements?: Record<string, string>): string {
  console.log('t function called with:', key, lang)
  try {
    const langTranslations = translations[lang]
    console.log('langTranslations:', langTranslations ? 'exists' : 'undefined')
    if (!langTranslations) {
      console.warn(`Language ${lang} not found`)
      return key
    }
    const translation = langTranslations[key as keyof typeof langTranslations]
    console.log('translation:', translation)
    if (!translation) {
      console.warn(`Translation key ${key} not found for language ${lang}`)
      return key
    }
    if (replacements) {
      return Object.entries(replacements).reduce(
        (str, [k, v]) => str.replace(`{${k}}`, v),
        translation as string
      )
    }
    return translation as string
  } catch (e) {
    console.error('Translation error:', e)
    return key
  }
}

